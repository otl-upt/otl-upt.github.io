  <h4>Our Version Numbers</h4>
  <hr />

  <p>
    We use
    <a href="https://semver.org/">Semantic Versioning</a>
    for our release numbers.
  </p>
  <p>
    Semantic versioning is a very simple scheme built around the X.Y.Z.buildNo
    concept:
  </p>
  <ul>
    <li>
      Increment X when you break backwards-compatibility or add major features
    </li>
    <li>Increment Y when you add a new feature</li>
    <li>Increment Z when you fix something or add minor features</li>
    <li>We set buildNo to the specific commit hash from git</li>
  </ul>

</div>
